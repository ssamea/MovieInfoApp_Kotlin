package com.example.movieapp

class Youtube {
    private val id: String? = null
    private val key: String? = null
    private val name: String? = null
    private val size: String? = null

    fun getId(): String? {
        return id
    }

    fun getKey(): String? {
        return key
    }

    fun getName(): String? {
        return name
    }

    fun getSize(): String? {
        return size
    }
}